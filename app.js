  const squares = [];
  // 0 - pac-dots
  // 1 - wall
  // 2 - ghost-lair
  // 3 - power-pellet
  // 4 - empty

  //Draw the grid and render it
  function createBoard() {
    for (let i = 0; i < layout.length; i++) {
      const square = document.createElement("div");
      grid.appendChild(square);
      squares.push(square);

      //add lyout ot board
      if (layout[i] === 0) {
        squares[i].classList.add("pac-dot");
      } else if (layout[i] === 1) {
        squares[i].classList.add("wall");
      } else if (layout[i] === 2) {
        squares[i].classList.add("ghost-lair");
      } else if (layout[i] === 3) {
        squares[i].classList.add("power-pellet");
      }
    }
  }
  createBoard();

  // create characters
  // draw pacman
  let pacmanCurrentIndex = 490
  squares[pacmanCurrentIndex].classList.add("pac-man")

  //move pacman

  function movePacman(e) {

    squares[pacmanCurrentIndex].classList.remove("pac-man")

    if (e.key === "ArrowLeft" && pacmanCurrentIndex % width !== 0 && !squares[pacmanCurrentIndex - 1].classList.contains("wall") && !squares[pacmanCurrentIndex - 1].classList.contains("ghost-lair")) {
      pacmanCurrentIndex -= 1;
      //check if pacman is in the left exit
      if ((pacmanCurrentIndex - 1) === 363) {
        pacmanCurrentIndex = 391;
      }
    } else if (e.key === "ArrowRight" && pacmanCurrentIndex % width < width - 1 && !squares[pacmanCurrentIndex + 1].classList.contains("wall") && !squares[pacmanCurrentIndex + 1].classList.contains("ghost-lair")) {
      pacmanCurrentIndex += 1;
      // check if pacman isn the right exit
      if ((pacmanCurrentIndex + 1) === 392) {
        pacmanCurrentIndex = 364;
      }
    } else if (e.key === "ArrowUp" && pacmanCurrentIndex - width >= 0 && !squares[pacmanCurrentIndex - width].classList.contains("wall") && !squares[pacmanCurrentIndex - width].classList.contains("ghost-lair")) {
      pacmanCurrentIndex -= width;
    } else if (e.key === "ArrowDown" && pacmanCurrentIndex + width < width * width && !squares[pacmanCurrentIndex + width].classList.contains("wall") && !squares[pacmanCurrentIndex + width].classList.contains("ghost-lair")) {
      pacmanCurrentIndex += width;
    }

    squares[pacmanCurrentIndex].classList.add("pac-man")
    pacDotEaten()
    powerPelletEaten()
    checkForWin()
    checkForGameOver()
  }

  document.addEventListener("keyup", movePacman);

  //what happens when the pacman eats the pac-dot.
  function pacDotEaten() {
    if (squares[pacmanCurrentIndex].classList.contains('pac-dot')) {
      score++
      scoreDisplay.innerHTML = score
      squares[pacmanCurrentIndex].classList.remove('pac-dot')
    }
  }

  // what happens when pacman eats a power-pellet
  function powerPelletEaten() {
    if (squares[pacmanCurrentIndex].classList.contains('power-pellet')) {
      score += 10
      ghosts.forEach(ghost => ghost.isScared = true)
      setTimeout(unScareGhosts, 10000)
      squares[pacmanCurrentIndex].classList.remove('power-pellet')
    }
  }

  //stops ghosts flashing
  function unScareGhosts() {
    ghosts.forEach(ghost => ghost.isScared = false)
  }

  //create ghosts template
  class Ghost {
    constructor(className, startIndex, speed) {
      this.className = className;
      this.currentIndex = startIndex;
      this.startIndex = startIndex;
      this.speed = speed;
      this.timerId = NaN;
      this.isScared = false;
    }
  }

  //all the ghosts
  ghosts = [
    new Ghost("blinky", 348, 250),
    new Ghost("pinky", 376, 400),
    new Ghost("inky", 351, 300),
    new Ghost("clyde", 379, 500)
  ]

  //Draw the ghosts onto the grid
  ghosts.forEach(ghost => {
    squares[ghost.currentIndex].classList.add(ghost.className)
    squares[ghost.currentIndex].classList.add('ghost')
  })

  //move ghost randomly
  ghosts.forEach(ghost => moveGhost(ghost))

  function moveGhost(ghost) {
    const directions = [-1, +1, width, -width];
    let direction = directions[Math.floor(Math.random() * directions.length)];

    ghosts.timerId = setInterval(function () {
      if (!squares[ghost.currentIndex + direction].classList.contains("wall") && !squares[ghost.currentIndex + direction].classList.contains("ghost")) {
        //remove the ghosts clases
        squares[ghost.currentIndex].classList.remove(ghost.className);
        squares[ghost.currentIndex].classList.remove("ghost", "scared-ghost")
        // move into that space
        ghost.currentIndex += direction;
        squares[ghost.currentIndex].classList.add(ghost.className, "ghost");
        //else find a new random direction to go in
      } else direction = directions[Math.floor(Math.random() * directions.length)];
      
      
      // if the ghost is scared change color
      if (ghost.isScared) {
        squares[ghost.currentIndex].classList.add('scared-ghost')
      }



      //if the ghost is currently scared and pacman is on it
      if(ghost.isScared && squares[ghost.currentIndex].classList.contains('pac-man')) {
        squares[ghost.currentIndex].classList.remove(ghost.className, 'ghost', 'scared-ghost')
        ghost.currentIndex = ghost.startIndex
        score +=100
        squares[ghost.currentIndex].classList.add(ghost.className, 'ghost')
      }
      checkForGameOver();
    }, ghost.speed)
  }

  // check if game is over
  function checkForGameOver() {
    if (squares[pacmanCurrentIndex].classList.contains('ghost') &&
      !squares[pacmanCurrentIndex].classList.contains('scared-ghost')) {
      ghosts.forEach(ghost => clearInterval(ghost.timerId))
      document.removeEventListener('keyup', movePacman)
      setTimeout(function(){ alert("Game Over"); }, 500)
    }
  }

  // check for win when 274 is reached all pacdots gone
  function checkForWin() {
    if (score === 274) {
      ghosts.forEach(ghost => clearInterval(ghost.timerId))
      document.removeEventListener('keyup', movePacman)
      setTimeout(function(){ alert("You have WON!"); }, 500)
    }
  }


